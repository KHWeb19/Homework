public class One {
    public static void main(String[] args) {

        //정수형 변수1개
        int a = 3;
        //float 변수2개
        float b1 = 3.3f;
        float b2 = 2.2f;
        //double 변수3개
        double c1 = 1.11;
        double c2 = 2.22;
        double c3 = 3.33;

        //정수형 변수 출력
        System.out.println("정수형 변수 : " + a);
        //float 변수 출력
        System.out.println("float 변수 : " + b1 + "/" + b2);
        //double 변수 출력
        System.out.println("double 변수 : " + c1 + "/" + c2 + "/"  + c3);
    }
}
