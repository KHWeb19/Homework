public class RelationOperationTest {
    public static void main(String[] args) {
        int num1 = 3, num2 = 7;
        boolean res;

        res = num1 == num2;
        System.out.println("res: " + res);

        res = num1 != num2;
        System.out.println("res: " + res);

        res = num1 < num2;
        System.out.println("res: " + res);

        res = num1 <= num2;
        System.out.println("res: " + res);

        res = num1 > num2;
        System.out.println("res: " + res);

        res = num1 >= num2;
        System.out.println("res: " + res);
    }
}
