import java.util.Scanner;

public class Student {
    // 학생의 기본적인 클래스
    private String name;
    private String dept;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

}
