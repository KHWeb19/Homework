public class JavaVariable {

    public static void main(String[] args) {

        // int, float 4byte , double 8byte
        // 1byte = 8bit

        int num;

        num = 3;

        // println - 자동엔터
        // printf - 포맷 형식을 지정 할 수 있음
        System.out.println("숫자 =" +num);
        System.out.printf("포맷 지정 숫자 = %d\n", num);
    }
}

